function info() {
    if (document.getElementById("body").style.display == "") {
        document.getElementById("body").style.display = "none";
        document.getElementById("opacitHeader").style.opacity = "1";
        document.getElementById("opacitSection").style.opacity = "1";
        document.getElementById("opacitFooter").style.opacity = "1";

       // document.getElementById("title").style.color = "black";
        
        } else {
        document.getElementById("body").style.display = "";
        //document.getElementById("title").style.color = "red";
        document.getElementById("opacitHeader").style.opacity = "0.1";
        document.getElementById("opacitSection").style.opacity = "0.1";
        document.getElementById("opacitFooter").style.opacity = "0.1";
        }
        return false;
       
    
}
